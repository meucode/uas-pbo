    private void loadTableData() {
    List<Mahasiswa> mahasiswaList = mahasiswaController.getAllMahasiswa();
    DefaultTableModel model = (DefaultTableModel) tbl_mahasiswa.getModel();
    model.setRowCount(0); // Clear existing data
    
    for (Mahasiswa mahasiswa : mahasiswaList) {
        model.addRow(new Object[]{mahasiswa.getNim(), mahasiswa.getNama(), mahasiswa.getKelas(), mahasiswa.getProdi(),mahasiswa.getTingkat(),mahasiswa.getPassword()});
    }
}
