    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        if(txt_nim.getText().equals("") || txt_nama.getText().equals("") || txt_kelas.getText().equals("") || txt_password.getText().equals("") || cmb_prodi.getSelectedItem().equals("") || cmb_tingkat.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(this, "Lengkapi data terlebih dahulu!");
        }else{
        Mahasiswa mahasiswa = new Mahasiswa(
            txt_nim.getText(),
            txt_nama.getText(),
            txt_kelas.getText(),
            cmb_prodi.getSelectedItem().toString(),
            cmb_tingkat.getSelectedItem().toString(),
            txt_password.getText()
    );
    mahasiswaController.tambahMahasiswa(mahasiswa);
    loadTableData();
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

