    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        String nim = txt_nim.getText();
        if(nim.equals("")){
            JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu!");
        }else{
            mahasiswaController.hapusMahasiswa(nim);
            loadTableData();
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void tbl_mahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_mahasiswaMouseClicked
        int selectedRow = tbl_mahasiswa.getSelectedRow();
    DefaultTableModel model = (DefaultTableModel) tbl_mahasiswa.getModel();
    
    // Mendapatkan nilai dari tabel berdasarkan baris yang diklik
    String nim = model.getValueAt(selectedRow, 0).toString();
    String nama = model.getValueAt(selectedRow, 1).toString();
    String kelas = model.getValueAt(selectedRow, 2).toString();
    String prodi = model.getValueAt(selectedRow, 3).toString();
    String tingkat = model.getValueAt(selectedRow, 4).toString();
    String password = model.getValueAt(selectedRow, 5).toString();
    
    // Menampilkan nilai di input field
    txt_nim.setText(nim);
    txt_nama.setText(nama);
    txt_kelas.setText(kelas);
    cmb_prodi.setSelectedItem(prodi);
    cmb_tingkat.setSelectedItem(tingkat);
    txt_password.setText(password);
    }//GEN-LAST:event_tbl_mahasiswaMouseClicked

